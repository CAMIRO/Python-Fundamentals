## most common string methods: 
    # upper() :makes all letters capitalize
    # lower() : makes all lower characters
    # capitalize() :makes the first letter capitalized
    # count() :count how many letters - words are in a text
    # find() : find the index of a character or a word
    # isdigit() : check if is a Num or not
    # isalum() : check if value is alpha numeric
    # isalpha() : check if is only letters
    # split() : separa por palabras utilizando espacios
    # strip() : delete extra spaces at the end and the beginning
    # replace() :replace words 
    # rfind() : same as find() but it does it in reverse
    



